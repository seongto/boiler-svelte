<h3 class="section-title">제12조 회원에 대한 통지</h3>
<div class="section-description"> 
    (1) 회사가 회원에 대한 통지를 하는 경우, 회원이 회사와 미리 약정하여 지정한 전자우편 주소, 팝업, 웹/앱 푸시알림, 전화로 할 수 있습니다.<br>
    (2) 회사는 불특정 다수 회원에 대한 통지의 경우, 1주일 이상 사이트에 게시함으로써 개별 통지에 갈음할 수 있습니다. 다만, 회원 본인의 거래와 관련하여 중대한 영향을 미치는 사항에 대하여는 개별 통지합니다.
</div>

<h3 class="section-title">제13조 회원의 게시물</h3>
<div class="section-description"> 
    (1) 회원이 서비스에 등록하는 게시물 및 타인 게시물의 활용 등으로 인하여 본인 또는 타인에게 손해나 기타 문제가 발생하는 경우 회원은 이에 대한 책임을 지게 되며, 회사는 특별한 사정이 없는 한 이에 대하여 책임을 지지 않습니다.<br>
    (2) 회사는 회원의 게시물이 다음 각 항에 해당한다고 판단되는 경우에 사전통지 없이 접근금지 또는 삭제처리를 할 수 있습니다. 단, 회사는 이러한 정보의 삭제 등을 할 의무를 지는 것은 아닙니다.
    <p class="list01">
        1. 다른 회원 또는 제3자를 비방하거나 명예를 훼손시키는 내용인 경우<br>
        2. 공공질서 및 미풍양속에 위반되는 내용인 경우<br>
        3. 범죄적 행위에 결부된다고 인정되는 내용일 경우<br>
        4. 회사 및 제3자의 저작권 등 기타 권리를 침해하거나 침해할 우려가 현저한 내용인 경우<br>
        5. 서비스 성격에 부합하지 않는 정보의 경우<br>
        6. 본 서비스 약관에 위배되거나 상용 또는 불법, 음란, 저속하다고 판단되는 게시물을 게시한 경우<br>
        7. 기타 관계 법령에 위배되는 경우<br>
        8. 회사가 상기 항목 위반을 이유로 회원 등 제3자로부터 신고를 접수한 경우
    </p>
    (4) 회원의 게시물이 정보통신망 이용촉진 및 정보보호 등에 관한 법률, 개인정보보호법 및 저작권법 등 관련법령에 위반되는 내용을 포함하는 경우, 권리자는 관련법령이 정한 절차에 따라 해당 게시물의 삭제 등을 요청할 수 있으며, 회사는 관련법령 내에서 회사가 정하는 절차에 따라 조치를 취할 수 있습니다.<br>
    (5) 회사는 전항에 따른 권리자의 요청이 없는 경우라도 권리침해가 인정될 만한 사유가 있거나 기타 회사 정책 및 관련법에 위반되는 경우에는 관련법령에 따라 해당 게시물에 대해 삭제나 접근금지 등의 조치를 취할 수 있습니다.<br>
    (6) 회원은 게시물이 타인의 저작권 및 기타 권리를 침해하지 않음을 보장하며, 게시물이 타인의 저작권 등을 침해함으로써 발생하는 민형사상의 책임을 전적으로 부담하여야 합니다.
</div>

<h3 class="section-title">제14조 권리의 귀속 및 저작물의 이용</h3>
<div class="section-description"> 
