    <p class="list01">
        - 구매를 원하는 서비스 대상 선택<br>
        - 회원의 성명, 이메일 주소, 전화번호(또는 이동전화번호), 주소 등의 입력 및 결제하기 클릭<br>
        - 결제금액 확인, 결제수단 선택 및 결제하기<br>
        - 결제금액 재확인 및 결제
    </p>
    (2) 회원은 회사에서 정하는 방법으로 유료서비스의 결제가 가능합니다. 단, 미성년 회원의 결제는 원칙적으로 보호자(법정대리인)의 명의 또는 동의 하에 이루어져야 하고, 보호자(법정대리인)는 본인 동의 없이 체결된 자녀(미성년자)의 계약을 취소할 수 있습니다.
</div>

<h3 class="section-title">제22조 계약의 성립 및 통지</h3>
<div class="section-description"> 
    (1) 회사는 다음 각호에 해당하지 않는 한 서비스 구매 신청을 승낙합니다.
    <p class="list01">
        1. 신청 내용에 허위, 누락, 오기가 있는 경우<br>
        2. 미성년자가 청소년 보호법에서 금지하는 게시물을 구매하는 경우<br>
        3. 유료 서비스 신청금액 총액과 입금 총액이 일치하지 않은 경우<br>
        4. 구매 신청에 승낙하는 것이 회사의 기술상 현저히 지장이 있다고 판단하는 경우<br>
        5. 회사가 정한 서비스 방침 또는 본 약관에 위배되지 않은 경우<br>
        6. 기타 합리적인 이유가 있는 경우로서 회사가 필요하다고 인정되는 경우
    </p>
    (2) 회사는 회원이 본 약관의 절차에 따라 유료서비스 이용을 신청할 경우, 승낙의 의사표시로써 본 약관의 방법을 통하여 회원에게 통지하고, 승낙의 통지가 회원에게 도달한 시점에 계약이 성립한 것으로 봅니다.<br>
    (3) 회사의 승낙의 의사표시에는 회원의 이용신청에 대한 확인 및 서비스제공 가능여부, 이용신청의 정정, 취소 등에 관한 정보 등을 포함합니다.
</div>

<h3 class="section-title">제23조 수수료 등</h3>
<div class="section-description"> 
    (1) 회원의 사이트 가입은 별도의 수수료 지급에 관한 서비스 안내가 없는 한 무료이며, 특정 서비스 이용 시 수수료가 부과될 수 있습니다.<br>
    (2) 서비스 수수료 정책은 회사 서비스에서 정의한 규칙에 따르며, 회원은 서비스 이용에 대한 수수료를 부담해야 합니다.<br>
    (3) 서비스 수수료 정책은 회사의 사정에 따라 변경될 수 있으며, 회사는 사전에 사이트를 통해 변경내용을 고지합니다.<br>
    (4) 회원은 회사의 서비스 수수료 외에 회원의 결제수단 (신용카드, 휴대폰, 계좌이체 등)이용에 따른 수수료를 부담해야 합니다.
</div>

<h3 class="section-title">제24조 제3자의 지식재산권 및 저작권 공지</h3>
<div class="section-description"> 
    (1) 회사와 회원은 제 3자의 지식재산을 존중하며, 회사는 제 3자의 지식재산권을 침해하는 회원과의 거래를 해지할 수 있습니다.<br>
    (2) 회사는 회원이 게시하고 등록한 콘텐츠가 제3자의 저작권을 침해했다고 통지 받는 경우, 저작권법 등 관련법령에 따라 해당 자료를 사이트에서 삭제 등 제한조치를 할 수 있습니다.<br>
    (3) 자신의 저작권이 침해되었다고 믿는 제 3자의 경우, 다음 정보를 포함하는 서면을 작성하여  회사가 공지한 메일주소로 제출하여야 합니다.
    <p class="list01">
        - 저작권 소유자 또는 법적 대리인의 서명<br>
        - 침해를 주장하는 콘텐츠<br>
        - 침해를 주장하는 콘텐츠가 게시 또는 사용된 사이트 및 앱 위치에 대한 상세한 기술<br>
        - 저작권자 또는 그 법적 대리인의 주소, 전화번호 및 이메일 주소<br>
        - 침해를 주장하는 서면의 모든 정보가 객관적 사실에 근거한 진실한 내용이며, 허위 또는 중요사실의 누락이 있는 경우 법적 책임을 부담한다는 저작권자 또는 법적 대리인의 진술내용
    </p>
    (4) 회원은 자신의 콘텐츠가 실수 또는 오인으로 인해 삭제 혹은 접근불가상태가 되었다고 믿는 경우, 다음 정보를 포함하는 서면을 작성하여 회사가 공지한 메일주소로 제출하여야 합니다.
    <p class="list01">
        - 회원의 서명<br>
        - 삭제 혹은 접근불가상태가 된 콘텐츠가 게시되어 있던 사이트 위치에 대한 상세한 기술<br>
        - 실수 또는 오인의 결과로 자신의 콘텐츠가 삭제 혹은 접근불가상태가 되었다고 믿는 진술과 허위 또는 중요사실의 누락이 있는 경우 법적 책임을 부담한다는 저작권자 또는 법적 대리인의 진술내용<br>
        - 회원의 성명, 주소, 전화번호 및 이메일 주소
    </p>
    (5) 회사는 위 절차에 따라 제출되지 아니한 경우 유효하지 않은 서면 통지로 간주하며, 이에 대한 응답 또는 조치를 취하지 않을 수 있습니다.
</div>

<h3 class="section-title">제25조 취소 및 환불</h3>
<div class="section-description"> 
