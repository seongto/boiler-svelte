    (2) 서비스를 이용하는 과정에서 회원이 직접 작성한 게시물, 댓글, 태그 등(이하 "게시물 등")에 대한 저작인격권은 별도의 의사표시가 없는 한 각 회원에게 귀속됩니다.<br>
    (3) 회원이 서비스가 제공되는 사이트에 게시물을 게시한 경우 회원은 회사 또는 다른 회원이 국내ㆍ외에서 다음 각 호의 목적으로 게시물 등을 사용하는 것을 허락한 것으로 봅니다.
    <p class="list01">
        1. 회사가 서비스(제3자가 운영하는 사이트 또는 미디어의 일정 영역 내에 입점하는 형태로 제공되는 경우 포함) 내에서 게시물 등의 복제·전송·전시 및 우수 게시물을 서비스 화면에 노출하기 위하여 게시물의 중대한 내용 변경 없이 크기를 변환하거나 단순화하는 등의 방식으로 수정하는 것<br>
        2. 회사가 운영하는 다른 서비스에서 게시물을 복제·전송 또는 전시하는 것. 다만, 회원이 명시적으로 복제·전송 또는 전시에 동의하지 아니한 경우에는 그러하지 않음<br>
        3. 회사의 서비스를 홍보하기 위한 목적으로 미디어·통신사 등에 게시물의 전부 또는 일부를 보도·방영하게 하는 것. 이 경우 회사는 원칙적으로 회원의 동의를 받아 미디어·통신사 등에게 회원정보를 제공하는 것<br>
        4. 회원의 프로폴리오 등을 포함한 게시물 및 해당 회원 정보(회원은 서비스 내의 관리기능을 통하여 공개 옵션 등을 설정한 것)가 다른 서비스 이용자로부터 검색 및 전송·열람·전시·공개되거나, 프로폴리오 등 게시물 및 서비스를 매개로 다른 회원과 연결(follow 등)되는 것. 다만, 회원이 명시적으로 연결에 동의하지 아니한 경우에는 그러하지 않음
    </p>
    (4) 회사는 서비스의 정책 또는 회사가 운영하는 서비스 간 통합 등의 사유로 게시물의 게재 위치를 변경·이전할 수 있습니다. <br>
    (5) 회원의 게시물에 관한 별도 규정이 있는 경우 이를 함께 준수하여야 하며, 본 조항과 상충하는 경우 관련 규정이 우선 적용됩니다.
</div>

<h3 class="section-title">제15조 개인정보보호</h3>
<div class="section-description"> 
