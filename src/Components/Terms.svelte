<h3 class="section-title">제1조 목적</h3>
<div class="section-description">
    본 약관은 주식회사 리얼비즌(이하 “회사”)가 운영하는 인터넷 사이트 얼라이어스 서비스(www.allius.io)(이하 “서비스”)를 이용함에 있어 회사와 이용자의 권리·의무 및 책임사항을 규정함을 목적으로 합니다.
</div>

<h3 class="section-title">제2조 용어의 정의</h3>
<div class="section-description">
    (1) 본 약관에서 사용하는 용어의 정의는 다음과 같습니다.
    <p class="list01">
        1. 이용자 : 서비스가 제공되는 사이트에 접속하여 본 약관에 따라 회사가 제공하는 서비스를 받는 회원 및 비회원을 말합니다.<br>
        2. 회원 : 회사에 개인정보를 제공하여 회원등록을 한 자로서 본 약관에 따라 회사와 서비스이용계약을 체결한 자를 말합니다.<br>
        3. ID : 회원의 식별과 서비스 이용을 위하여 회원이 선정하고 회사가 승인한 이메일 등을 말합니다.<br>
        4. 비밀번호 : 회원의 동일성 확인과 회원의 권익 및 비밀보호를 위하여 회원 스스로가 설정하여 회사에 등록한 영문 또는 숫자의 조합을 말합니다. <br>
        5. 프로폴리오 정보 : 회원이 서비스를 이용하기 위하여 회원이 입력한 이름, 이메일, 하는 일과 역할, 브랜드, 소속, 프로필 이미지, 인용 문구, 휴대전화번호, 주소 등 프로폴리오 서비스에 기재되는 회원이 제공한 회원의 개인정보를 말합니다. <br>
        6. 게시물 : 회원이 서비스를 이용하면서 게시한 글, 이미지, 동영상, 음성, 각종 파일과 링크 등을 의미하며, 회원이 다른 사이트에서 작성하거나 게시한 저작물을 인용하거나 링크를 공유함으로써 게시한 내용(프로젝트, 콘텐츠 등)도 이에 포함됩니다. 
    </p>
    (2) 본 약관에 명시되지 않은 용어는 개별약관 또는 관련 법령이 정하는 바에 따르며, 그 외에는 일반적인 상거래관행에 의합니다.
</div>

<h3 class="section-title">제3조 약관의 명시와 개정</h3>
<div class="section-description"> 
    (1) 회사는 본 약관을 이용자가 알 수 있도록 사이트의 서비스 화면에 게시합니다. 다만, 회사는 제공하는 개별 서비스에 대한 약관 및 이용조건을 각 개별서비스마다 별도로 정하여 회원의 동의를 얻을 수 있습니다. 이 경우 개별 서비스에 대한 이용약관 등이 본 약관에 우선하며 약관의 구체적 내용은 이용자가 연결화면을 통하여 볼 수 있도록 합니다.<br>
    (2) 회사는 약관의 규제에 관한 법률, 콘텐츠산업 진흥법, 전자문서 및 전자거래 기본법, 전자서명법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률, 전자상거래 등에서의 소비자보호에 관한 법률, 유사수신행위의 규제에 관한 법률 등 관련법을 위배하지 않는 범위에서 본 약관을 개정할 수 있습니다.<br>
    (3) 회사가 본 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 현행 약관과 함께 사이트의 초기화면에 최소 그 적용일자 7일 이전부터 적용일자 전일까지 공지합니다. 다만, 이용자에게 불리하게 약관의 내용을 변경하는 경우에는 적용일자 15일 이전부터 공지합니다.<br>
    (4) 제3항에 따라 공지된 적용일자 이후에 회원이 회사의 서비스를 계속 이용하는 경우에는 개정된 약관에 동의하는 것으로 봅니다. 개정된 약관에 동의하지 아니하는 회원은 언제든지 자유롭게 서비스 이용계약을 해지할 수 있습니다. 유료서비스를 이용하는 회원이 약관의 개정에 동의하지 아니하여 해지할 경우 회사가 정한 환불정책에 따릅니다.<br>
    (5) 본 약관에서 정하지 아니한 사항과 본 약관의 해석에 관하여는 정부가 제정한 전자거래소비자보호지침 및 관계법령 또는 일반 상 관례에 따릅니다. 
</div>

<h3 class="section-title">제4조 서비스의 제공 및 변경</h3>
<div class="section-description"> 
    (1) 회사는 다음과 같은 서비스를 제공합니다.
    <p class="list01">
        1. 회원의 프로젝트, ‘나의브랜드', 프로폴리오 정보 작성 및 게시, 공유 서비스<br>
        2. 회원이 작성하거나 링크한 프로젝트 또는 콘텐츠 정보 게시, 관리, 공유 서비스<br>
        3. 회원의 네트워크 정보 게시, 관리, 공유 서비스<br> 
        4. 회원의 ‘프로젝트', ‘프로폴리오', ‘나의브랜드' 콘텐츠 개발 및 작성, 컨설팅 서비스<br>
        5. 회사가 추가 개발하거나 다른 회사와의 제휴 등을 통해 회원에게 제공하는 일체의 서비스<br>
        6. 이에 부수되는 서비스 및 기타 회사가 정하는 업무
    </p>
    (2) 회사는 회원의 이용신청을 승낙한 때부터 서비스를 개시합니다. 단, 일부 서비스의 경우에는 지정된 일자부터 또는 일정 요건 충족의 경우에 한하여 서비스를 개시합니다.<br>
    (3) 회사의 업무상 또는 기술상의 장애로 인하여 서비스를 개시하지 못하는 경우에는 사이트에 공시하거나 회원에게 이를 통지합니다.<br>
    (4) 서비스의 이용은 연중무휴, 1일 24시간을 원칙으로 합니다. 다만, 회사의 업무상 또는 기술상의 이유로 서비스가 일시 중지 될 수 있습니다. 이러한 경우 회사는 사전 또는 사후에 이를 공지합니다.<br>
    (5) 일부 서비스에 대해서는 서비스 제공자의 요구에 따라 특정회원에게만 서비스를 제공할 수 있으며, 회사는 서비스를 일정범위로 분할하여 각 범위 별로 이용가능 시간을 별도로 정할 수 있습니다. 이 경우 그 내용을 사전에 공개합니다.
</div>

<h3 class="section-title">제5조 서비스 이용제한</h3>
<div class="section-description"> 
    (1) 이용자가 제공하는 정보의 내용이 허위인 것으로 판명되거나, 허위가 있다고 의심할 만한 합리적인 사유가 발생할 경우 회사는 이용자의 본 서비스 사용을 일부 또는 전부 이용 정지할 수 있으며, 이로 인해 발생하는 불이익에 대해 회사는 책임을 부담하지 아니합니다.<br>
    (2) 회사는 이용자가 본 약관의 이용자의 의무 등 본 약관의 내용에 위배되는 행동을 한 경우, 임의로 서비스 사용을 제한 및 중지할 수 있습니다. 이 경우 회사는 이용자의 접속을 금지할 수 있습니다.
</div>

<h3 class="section-title">제6조 서비스의 중단</h3>
<div class="section-description"> 
    (1) 천재지변, 국가비상사태 등 회사가 정상적인 서비스 제공이 불가능할 경우 일시적으로 서비스를 제한, 정지시킬 수 있으며 사전 또는 사후 회원에게 중지사유 및 기간을 공지합니다. 회사는 위 수정, 변경 및 중단에 대하여 회원 또는 제3자에 어떠한 책임도 부담하지 않습니다.<br>
    (2) 회사는 긴급한 컴퓨터 등 정보통신설비의 보수점검, 교체 및 고장, 통신두절 또는 운영상 상당한 이유가 있는 경우 콘텐츠의 이용을 일시적으로 중단할 수 있습니다. 서비스 중지 1주일 전에 고지 후 서비스를 중지할 수 있으며, 이 기간 동안 회원이 고지내용을 인지하지 못한 것에 대하여 회사는 책임을 부담하지 아니합니다. 상당한 이유가 있을 경우 위 사전 고지기간은 감축되거나 생략될 수 있습니다. 다만, 회사가 사전에 통지할 수 없는 부득이한 사유가 있는 경우 사후에 통지할 수 있습니다.<br>
    (3) 회사는 서비스 내용의 결정, 변경, 유지, 중단에 관한 포괄적인 권한을 가집니다. 회사는 영업의 폐지, 합병, 분할, 당해 서비스의 수익악화, 서비스 유지 등 상당한 이유가 있는 경우 제공하고 있는 전부 또는 일부 서비스를 변경하거나 중단할 수 있습니다.<br>
    (4) 회사의 사정으로 서비스를 영구적으로 중단하여야 할 경우에 제2항을 준용합니다. 다만, 이 경우 사전 고지기간은 1개월로 합니다.<br>
    (5) 회사는 서비스 중지에 의하여 본 서비스에 보관되거나 전송된 메시지 및 기타 통신 메시지 등의 내용이 보관되지 못하였거나 삭제된 경우, 전송되지 못한 경우 및 기타 통신 데이터의 손실이 있을 경우에 대하여 책임을 부담하지 않습니다.<br>
    (6) 회사는 서비스의 이용에 필요한 경우 정기점검을 실시할 수 있으며, 정기점검시간은 서비스 제공화면에 공지한 바에 따릅니다.<br>
    (7) 장기간 휴면 회원인 경우, 안내 메일 또는 공지사항 발표 후 1주일간의 통지기간을 거쳐 서비스 사용을 중지할 수 있습니다.
</div>

<h3 class="section-title">제7조 회원가입 등 이용계약의 성립</h3>
<div class="section-description"> 
    (1) 이용자는 회사가 정한 가입양식에 따라 회원 정보를 기입한 후 본 약관에 동의한다는 의사표시를 함으로서 회원가입을 신청합니다. 회사는 이용자가 기입한 내용에 대해 전문기관을 통한 본인인증을 요청할 수 있습니다.<br>
    (2) 회사는 유료 서비스에 관하여 만 14세 미만의 이용자에 대한 서비스 이용을 제한할 수 있습니다. <br>
    (3) 회사는 다음에 해당하는 경우에는 이용신청을 승낙하지 아니할 수 있습니다.
    <p class="list01">
        1. 이미 가입된 회원과 이메일 주소가 동일한 경우<br>
        2. 회원가입 신청서의 내용을 허위로 기재하였을 때<br>
        3. 타인의 정보를 도용하여 신청하였을 때<br>
        4. 사회의 안녕 질서 혹은 미풍양속을 저해할 목적으로 신청했을 때<br>
        5. 기타 회사가 정한 이용신청 요건이 미비되었을 때
    </p>
    (4) 회사는 다음 각 호에 해당하는 경우 그 사유가 해소될 때까지 이용계약 성립을 유보 또는 일부 서비스 이용을 제한할 수 있습니다.
    <p class="list01">
        1. 서비스 관련 제반 용량이 부족한 경우<br>
        2. 기술상 장애 사유가 있는 경우<br>
        3. 기타 회사가 필요하다고 인정한 경우
    </p>
    (5) 이용계약의 성립 시기는 회사의 승낙 통지가 회원에게 도달한 시점으로 합니다.
</div>

<h3 class="section-title">제8조 회원 탈퇴 및 자격 상실</h3>
<div class="section-description"> 
    (1) 회원은 회사에 회원 탈퇴를 요청할 수 있으며, 회사는 회원탈퇴에 관한 규정에 따라 이를 처리합니다. 회사는 회원이 회원탈퇴를 신청한 경우 회사는 회원 본인 여부를 확인할 수 있습니다.<br>
    (2) 회원이 다음 각 사유에 해당될 때에는 회사는 탈퇴 요청을 처리하지 않을 수 있습니다.
    <p class="list01">
        1. 회원이 회사가 제공하는 서비스에 참여 중인 경우<br>
        2. 회원이 진행하거나 참여한 프로젝트가 진행 중인 경우<br>
        3. 회원이 회사가 제공하는 서비스와 관련한 분쟁이 회사 또는 제3자와 진행 중인 경우<br>
        4. 기타 회원이 부담해야 할 의무를 다하지 않은 경우
    </p>
    (3) 회사가 회원 탈퇴 처리하면 관계법령과 개인정보취급방침에 따라 회사가 보유하는 정보 이외 데이터를 모두 폐기합니다. 단, 회원의 게시물은 삭제되지 않을 수 있습니다.<br>
    (4) 회사는 회원이 다음 각 호의 사유에 해당하는 경우, 사전통지 없이 회원자격을 제한 및 정지시킬 수 있습니다.
    <p class="list01">
        1. 가입신청 시에 허위 내용을 등록한 경우<br>
        2. 서비스 이용과 관련하여 회원이 부담하는 채무를 약정된 기일에 지급하지 않은 경우<br>
        3. 다른 사람의 서비스 이용을 방해하거나 그 정보를 도용하는 등 전자거래질서를 위협하는 경우<br>
        4. 서비스를 이용하여 법령과 본 약관이 금지하거나 공공질서 미풍양속에 반하는 행위를 하는 경우
    </p>
    (5) 회사가 회원자격을 제한 또는 정지시킨 후 동일 또는 유사한 행위가 2회 이상 반복되거나 7일 이내에 그 사유가 시정되지 아니하는 경우 회사는 회원자격을 상실시킬 수 있습니다. 이 경우 회사는 회원의 접속을 금지할 수 있으며, 회원이 게시한 내용의 전부 또는 일부를 임의로 삭제할 수 있습니다.<br>
    (6) 회사가 회원자격을 상실시키는 경우에는 회원등록을 말소합니다. 이 경우 회원에게 이를 통지하고, 회원등록 말소 전에 소명할 기회를 부여합니다.
</div>

<h3 class="section-title">제9조 회원의 ID와 비밀번호에 대한 의무</h3>
<div class="section-description"> 
    (1) ID와 비밀번호에 관한 비밀유지 및 관리책임은 회원에게 있으며, 이를 소홀히 하여 발생한 모든 민∙형사상의 책임은 회원 자신에게 있습니다. <br>
    (2) 회원은 사이트 내에서 ID와 비밀번호의 관리 및 변경을 할 수 있습니다. 이용신청시의 기재사항에 변동이 있을 경우 회원은 지체없이 해당 정보를 수정해야 하며, 전자우편 기타 방법으로 회사에 대하여 그 변경사항을 알려야 합니다. <br>
    (3) 회원은 자신의 ID 및 비밀번호를 제3자에게 이용하게 해서는 안됩니다.<br>
    (4) 회원이 자신의 ID 및 비밀번호를 도난 또는 도용 당하거나 제3자가 사용하고 있음을 인지한 경우에는 즉시 회사에 통보하고 회사의 안내가 있는 경우에는 그에 따라야 합니다.<br>
    (5) 회원이 제2항, 제4항에 따른 통지를 하지 않거나 회사의 조치에 응하지 아니하여 발생하는 모든 불이익에 대한 책임은 회원에게 있습니다.
</div>

<h3 class="section-title">제10조 회사의 의무</h3>
<div class="section-description">
    (1) 회사는 법령과 본 약관이 금지하거나 미풍양속에 반하는 행위를 하지 않으며, 지속적이고 안정적으로 서비스를 제공하는 데에 최선을 다하여야 합니다.<br>
    (2) 회사는 회원의 개인 신상 정보를 본인의 승낙 없이 제3자에게 누설, 배포하지 않습니다. 다만, 관계법령에 의해 국가기관의 요구나 전기통신관련법령 및 방송통신심의위원회의 요청 등 법률의 규정에 따른 적법한 절차에 의한 경우에는 그러하지 아니합니다.<br>
    (3) 회사는 계속적이고 안정적인 서비스의 제공을 위하여 설비에 장애가 생기거나 멸실된 때에는 부득이한 사유가 없는 한 지체없이 이를 수리 또는 복구합니다.<br>
    (4) 회사가 제공하는 유료서비스로 인하여 회원에게 손해가 발생한 경우 그러한 손해가 회사의 고의나 중과실에 기해 발생한 경우에 한하여 책임을 부담하며, 그 책임의 범위는 지급한 유료서비스의 이용료에 한합니다.<br>
    (5) 회사는 회원으로부터 제기되는 의견이나 불만이 정당하다고 인정할 경우에는 신속히 처리하여야 합니다. 다만 신속한 처리가 곤란한 경우에는 회원에게 그 사유와 처리일정을 통보하여야 합니다. <br>
    (6) 회사는 관련법령이 정하는 바에 따라서 회원 등록정보를 포함한 회원의 개인정보를 보호하기 위하여 노력하며, 회원의 개인정보보호에 관해서는 관련법령 및 개인정보취급방침을 지킵니다.
</div>

